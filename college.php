            <p>From 2013 the college has begun holding
            an annual lecture , dedicated to the memory
            of its founding father, Dr. Alexander Duff,
            to be delivered by an eminent personality.
            In the fitness of things the First
            Alexander Duff Memorial Lecture, held
            th on 13th July, 2013, as part of the 184
            Foundation Day Celebration, was delivered
            by the then Governor of Odisha and our
            alumnus, Dr. S. C. Jamir.</p><br>

            <p>The Third Dr. Alexander Duff Memorial
            Lecture, held on 25th August 2015, was
            delivered by Dr. Bikash Sinha, Indian
            National Science Academy Emeritus
            Scientist &amp; Former Homi Bhabha
            Professor, DAE, Former Director, Saha
            Institute of Nuclear Physics &amp; Variable
            Energy Cyclotron Centre Kolkata, on the
            'Neoteric Culture of Education'. He spoke
            on how the contemporary structure of
            education is totally designed as a degree
            factory; the holistic framework tends to get
            overwhelmed by narrow horizons turning
            the "culture" of education devoid of any
            culture. Tracing the history of science from
            ancient days to now he elaborated on how
            the two "cultures" – one of science and the
            other of humanities – have drifted apart
            from each other. The resultant void tends to
            produce men and women without vision or
            courage. What can be done to make our
            education more holistic ? Dr. Sinha pointed
            out how this was done brilliantly by the
            founder of the college Dr. Alexander Duff.</p>
